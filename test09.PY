# สร้างโปรแกรมคำนวณเงินเดือนสุทธิที่ต้องจ่ายหลังจากหักภาษีแล้ว 7% ของเงินเดือน และหักค่าประกันสังคม 3% ของเงินเดือน 
# โดยรับค่ารหัสพนักงาน ชื่อพนักงาน เงินเดือน ทางแป้นพิมพ์ และแสดงผลข้อมูลที่รับมา 
# พร้อมกับรายละเอียดว่าต้องเสียภาษีกี่บาท หักค่าประกันสังคมกี่บาท และต้องจ่ายเงินเดือนสุทธิกี่บาท

print("++++++++++++++++++")
print("โปรแกรมคํานวณเงินเดือน")
print("++++++++++++++++++")
emp_code = input("ป้อนรหัสพนักงาน: ")
emp_name = input("ป้อนชื่อพนักงาน: ")
emp_salary = input("ป้อนเงินเดือน: ")
print("++++++++++++++++++")
tax = float(emp_salary) * 0.07
insurance = float(emp_salary) * 0.03
net_salary = float(emp_salary) - tax - insurance
print(f"รหัสพนักงาน: {emp_code} ชื่อ: {emp_name} เงินเดือน: {emp_salary}")
print(f"ต้องเสียภาษี {tax} บาท")
print(f"หักค่าประกันสังคม {insurance} บาท")
print(f"ต้องจ่ายเงินเดือนสุทธิ {net_salary} บาท")
print("++++++++++++++++++") 
#ใช้ ,
print("รหัสพนักงาน:", emp_code, "ชื่อ:" ,emp_name, "เงินเดือน:" ,emp_salary, "บาท")
print("ต้องเสียภาษี" ,tax, "บาท")
print("หักค่าประกันสังคม" ,insurance, "บาท")
print("ต้องจ่ายเงินเดือนสุทธิ" ,net_salary, "บาท")
print("++++++++++++++++++")
#ใช้ +
print("รหัสพนักงาน: " + str(emp_code) + " ชื่อ: " + str(emp_name) + " เงินเดือน: " + str(emp_salary) + " บาท")
print("ต้องเสียภาษี " + str(tax) + " บาท")
print("หักค่าประกันสังคม " + str(insurance) + " บาท")
print("ต้องจ่ายเงินเดือนสุทธิ " + str(net_salary) + " บาท")
print("++++++++++++++++++")
#ใช้ format
print("รหัสพนักงาน: {} ชื่อ: {} เงินเดือน: {} บาท".format(emp_code, emp_name, emp_salary))
print("ต้องเสียภาษี {} บาท".format(tax))
print("หักค่าประกันสังคม {} บาท".format(insurance))
print("ต้องจ่ายเงินเดือนสุทธิ {} บาท".format(net_salary))
print("++++++++++++++++++")